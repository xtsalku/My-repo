public class operatoraritmatika {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
    int i = 10;
    int j = 20;
    double x = 3.21;
    double y = 4.34;

      System.out.println ("-----------------------------");
      System.out.println ("nilai variabel");
      System.out.println ("-----------------------------");
      System.out.println ("i = " + i);
      System.out.println ("j = " + j);
      System.out.println ("x = " + j);
      System.out.println ("y = " + j);
      System.out.println ();

      System.out.println ("-----------------------------");
      System.out.println ("pertambahan");
      System.out.println ("-----------------------------");
      System.out.println ("i+j = " + (i + j));
      System.out.println ("x+y = " + (x + y));
      System.out.println ();

      System.out.println ("-----------------------------");
      System.out.println ("pengurangan");
      System.out.println ("-----------------------------");
      System.out.println ("i-j = " + (i - j));
      System.out.println ("x-y = " + (x - y));
      System.out.println ();

      System.out.println ("------------------------------");
      System.out.println ("perkalian");
      System.out.println ("-----------------------------");
      System.out.println ("i*j = " + (i * j));
      System.out.println ("x*y = " + (x * y));
      System.out.println ();

      System.out.println ("-----------------------------");
      System.out.println ("pembagian");
      System.out.println ("-----------------------------");
      System.out.println ("i/j = " + (i / j));
      System.out.println ("x/y = " + (x / y));
      System.out.println ();

      System.out.println ("-----------------------------");
      System.out.println ("modulus");
      System.out.println ("-----------------------------");
      System.out.println ("i%j = " + (i % j));
      System.out.println ("x%y = " + (x % y));
      System.out.println ();

      System.out.println ("-----------------------------");
      System.out.println ("campuran operator");
      System.out.println ("-----------------------------");
      System.out.println ("i+j = " + (i + j));
      System.out.println ("x*y = " + (x * y));
      System.out.println ();

  }
}

   
