public class operatorkurangdanlebih {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int i = 5;
    int j = 6;
    int k = 7;

      System.out.println ("deklarasi variabel");
      System.out.println ("i = " + i);
      System.out.println ("j = " + j);
      System.out.println ("x = " + j);
      System.out.println ("y = " + j);
      System.out.println ();

      System.out.println ("operator lebih besar dari...");
      System.out.println ("i > j = " + (i > j));
      System.out.println ("j > i = " + (j > i));
      System.out.println ("k > j = " + (k > j));
      System.out.println ();

      System.out.println ("operator lebih besar dari sama dengan...");
      System.out.println ("i >= j = " + (i >= j));
      System.out.println ("j >= i = " + (j >= i));
      System.out.println ("k >= j = " + (k >= j));
      System.out.println ();

      System.out.println ("operator kurang dari...");
      System.out.println ("i < j = " + (i > j));
      System.out.println ("j < i = " + (j < i));
      System.out.println ("k < j = " + (k < j));
      System.out.println ();

      System.out.println ("operator kurang dari sama dengan...");
      System.out.println ("i <= j = " + (i <= j));
      System.out.println ("j <= i = " + (j <= i));
      System.out.println ("k <= j = " + (k <= j));
      System.out.println ();

      System.out.println ("operator pembanding");
      System.out.println ("i == j = " + (i == j));
      System.out.println ("k == j = " + (k == j));
      System.out.println ();

      System.out.println ("operator NOT");
      System.out.println ("i != j = " + (i != j));
      System.out.println ("k != j = " + (k != j));
      System.out.println ();
    }
    
}
